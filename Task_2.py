x = 5
y = 6

a = x & y
b = x | y
c = x ^ y
d = x << 2
e = x >> 2

print(f'Побитовое & (И) чифр {x} и {y} = {a}')
print(f'Побитовое | (ИЛИ) чифр {x} и {y} = {b}')
print(f'Исключающее ^ (ИЛИ) чифр {x} и {y} = {c}')
print(f'Побитовый сдвиг влево на два знака << для чифры {x}  = {d}')
print(f'Побитовый сдвиг вправо на два знака >> для чифры {x}  = {e}')
